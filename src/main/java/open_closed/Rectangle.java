package open_closed;

public class Rectangle implements Shape, ShapePerim {

    private double width;
    private double height;

    public Rectangle(double width, double height) {
        this.width = width;
        this.height = height;
    }

    @Override
    public double calculateArea() {
        return width * height;
    }

    @Override
    public double calculatePerim() {
        return 0;
    }
}
